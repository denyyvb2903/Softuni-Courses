function demo(language) {

    if (language == `USA`) {

        console.log(`English`);

    } else if (language == `England`) {

        console.log(`English`)

    } else if (language == `Spain`) {

        console.log(`Spanish`)

    } else if (language == `Argentina`) {

        console.log(`Spanish`)

    } else if (language == `Mexico`) {

        console.log(`Spanish`)

    } else {

        console.log(`unknown`)

    }

}
demo([`Mexico`])